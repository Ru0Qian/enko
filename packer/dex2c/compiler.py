# Default ABIs to build for.
DEFAULT_ABIS = ["arm64-v8a", "armeabi-v7a", "x86", "x86_64"]


def _iter_sdk_roots() -> list[Path]:
    roots: list[Path] = []
    for value in (
        os.environ.get("ANDROID_SDK_ROOT", ""),
        os.environ.get("ANDROID_HOME", ""),
        r"D:\Env\tool\Android-Sdk",
        r"D:\Android\Sdk",
        str(Path.home() / "AppData" / "Local" / "Android" / "Sdk"),
    ):
        if not value:
            continue
        path = Path(value).expanduser()
        if path.exists() and path not in roots:
            roots.append(path)
    return roots


def _find_cmake_from_ndk(ndk_path: str) -> str:
    """Locate cmake from NDK, Android SDK, or PATH."""
    ndk = Path(ndk_path)
    # NDK bundles cmake under <ndk>/cmake/<version>/bin/cmake(.exe)
    cmake_dir = ndk / "cmake"
    if cmake_dir.is_dir():
        for ver_dir in sorted(cmake_dir.iterdir(), reverse=True):
            for name in ("cmake.exe", "cmake"):
                candidate = ver_dir / "bin" / name
                if candidate.is_file():
                    return str(candidate)
    # Newer setups often install CMake under Android SDK instead of inside NDK.
    for sdk_root in _iter_sdk_roots():
        sdk_cmake_dir = sdk_root / "cmake"
        if not sdk_cmake_dir.is_dir():
            continue
        for ver_dir in sorted(sdk_cmake_dir.iterdir(), reverse=True):
            for name in ("cmake.exe", "cmake"):
                candidate = ver_dir / "bin" / name
                if candidate.is_file():
                    return str(candidate)
    # Fallback: system cmake
    found = shutil.which("cmake")
    if found:
        return found
    raise RuntimeError("cmake not found in NDK, Android SDK, or PATH")


def _find_ninja_for_cmake(cmake_bin: str) -> str | None:
    cmake_path = Path(cmake_bin).resolve()
    for name in ("ninja.exe", "ninja"):
        sibling = cmake_path.parent / name
        if sibling.is_file():
            return str(sibling)
    found = shutil.which("ninja")
    if found:
        return found
    return None


def _find_toolchain_file(ndk_path: str) -> str:
    tc = Path(ndk_path) / "build" / "cmake" / "android.toolchain.cmake"
    if not tc.is_file():
        raise RuntimeError(f"NDK toolchain file not found: {tc}")
    return str(tc)


def _extract_error_excerpt(output: str, context_after: int = 2) -> str:
    lines = output.splitlines()
    for idx, line in enumerate(lines):
        lowered = line.lower()
        if " error:" in lowered or lowered.startswith("error:") or "fatal error:" in lowered:
            end = min(len(lines), idx + 1 + context_after)
            return "\n".join(lines[idx:end])
    return ""


def _preserve_failed_build(build_root: str, abi: str, phase: str, output: str) -> str:
    dest = Path(tempfile.gettempdir()) / f"enko_d2c_failed_{uuid.uuid4().hex[:8]}"
    shutil.copytree(build_root, dest, dirs_exist_ok=True)
    summary = dest / "failure_summary.txt"
    summary.write_text(
        f"phase: {phase}\nabi: {abi}\n\n{output}",
        encoding="utf-8",
    )
    return str(dest)


def compile_with_ndk(
    c_source: str,
    ndk_path: str,
    output_dir: Path,
    abis: list[str] | None = None,
    ollvm_enabled: bool = True,
    ollvm_clang: str | None = None,
    ollvm_required: bool = False,
    build_report: dict[str, object] | None = None,
) -> dict[str, Path]:
    """Compile the generated C source into libagpjnix.so for each ABI.

    Returns a dict of ``{abi: path_to_so}``.
    """
    if abis is None:
        abis = list(DEFAULT_ABIS)

    cmake_bin = _find_cmake_from_ndk(ndk_path)
    ninja_bin = _find_ninja_for_cmake(cmake_bin)
    toolchain = _find_toolchain_file(ndk_path)
    ollvm_clang = (ollvm_clang or DEFAULT_OLLVM_CLANG).strip()
    ollvm_clang_available = bool(ollvm_clang and Path(ollvm_clang).is_file())

    results: dict[str, Path] = {}
    if build_report is not None:
        build_report.update(
            {
                "requested": bool(ollvm_enabled),
                "required": bool(ollvm_required),
                "clang": ollvm_clang,
                "clang_exists": ollvm_clang_available,
                "per_abi": [],
                "ollvm_protected_libraries": [],
                "ollvm_protected_abis": [],
                "fallback_libraries": [],
                "fallback_abis": [],
                "fallback_used": False,
            }
        )
    build_env = os.environ.copy()
    if ninja_bin:
        build_env["PATH"] = str(Path(ninja_bin).parent) + os.pathsep + build_env.get("PATH", "")
    preserved_failure_dir: str | None = None
    if ollvm_enabled:
        mode = "required" if ollvm_required else "best-effort"
        print(f"[d2c] Hikari/OLLVM requested for libagpjnix.so ({mode}): {ollvm_clang}")
        if not ollvm_clang_available and not ollvm_required:
            print("[!] d2c Hikari/OLLVM clang missing before configure; using normal NDK compile")

    with tempfile.TemporaryDirectory(prefix="enko_d2c_build_") as build_root:
        src_dir = Path(build_root) / "src"
        src_dir.mkdir()
        (src_dir / "enko_dex2c.c").write_text(c_source, encoding="utf-8")
        (src_dir / "CMakeLists.txt").write_text(_CMAKE_TEMPLATE, encoding="utf-8")

        for abi in abis:
            attempts = [True] if ollvm_enabled and (ollvm_clang_available or ollvm_required) else [False]
            if ollvm_enabled and not ollvm_required and ollvm_clang_available:
                attempts.append(False)
            abi_built = False

            for use_ollvm in attempts:
                suffix = "ollvm" if use_ollvm else "ndk"
                build_dir = Path(build_root) / f"build_{abi}_{suffix}"
                build_dir.mkdir()

                # Configure.
                configure_cmd = [
                    cmake_bin,
                    "-GNinja",
                    f"-DCMAKE_TOOLCHAIN_FILE={toolchain}",
                    f"-DANDROID_ABI={abi}",
                    "-DANDROID_PLATFORM=android-26",
                    "-DCMAKE_BUILD_TYPE=Release",
                    f"-DENKO_D2C_OLLVM={'ON' if use_ollvm else 'OFF'}",
                    f"-DENKO_D2C_OLLVM_REQUIRED={'ON' if ollvm_required else 'OFF'}",
                    f"-DENKO_D2C_OLLVM_CLANG={ollvm_clang}",
                    f"-B{build_dir}",
                    f"-S{src_dir}",
                ]
                if ninja_bin:
                    configure_cmd.append(f"-DCMAKE_MAKE_PROGRAM={ninja_bin}")
                print(f"[d2c] cmake configure {abi} ({suffix})")
                r = subprocess.run(
                    configure_cmd,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.STDOUT,
                    text=True,
                    encoding="utf-8",
                    errors="replace",
                    env=build_env,
                )
                if r.returncode != 0:
                    print(f"[!] d2c cmake configure failed for {abi} ({suffix}):\n{r.stdout}")
                    excerpt = _extract_error_excerpt(r.stdout)
                    if excerpt:
                        print(f"[!] d2c first configure error for {abi} ({suffix}):\n{excerpt}")
                    if use_ollvm and not ollvm_required:
                        print(f"[!] d2c Hikari configure failed for {abi}; retrying normal NDK compile")
                        continue
                    if preserved_failure_dir is None:
                        preserved_failure_dir = _preserve_failed_build(
                            build_root, abi, f"configure-{suffix}", r.stdout
                        )
                        print(f"[!] d2c preserved failed build tree: {preserved_failure_dir}")
                    continue

                # Build.
                build_cmd = [cmake_bin, "--build", str(build_dir), "--config", "Release"]
                print(f"[d2c] cmake build {abi} ({suffix})")
                r = subprocess.run(
                    build_cmd,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.STDOUT,
                    text=True,
                    encoding="utf-8",
                    errors="replace",
                    env=build_env,
                )
                if r.returncode != 0:
                    print(f"[!] d2c cmake build failed for {abi} ({suffix}):\n{r.stdout}")
                    excerpt = _extract_error_excerpt(r.stdout)
                    if excerpt:
                        print(f"[!] d2c first build error for {abi} ({suffix}):\n{excerpt}")
                    if use_ollvm and not ollvm_required:
                        print(f"[!] d2c Hikari build failed for {abi}; retrying normal NDK compile")
                        continue
                    if preserved_failure_dir is None:
                        preserved_failure_dir = _preserve_failed_build(
                            build_root, abi, f"build-{suffix}", r.stdout
                        )
                        print(f"[!] d2c preserved failed build tree: {preserved_failure_dir}")
                    continue

                # Find the output .so.
                so_candidates = list(Path(build_dir).rglob("libagpjnix.so"))
                if not so_candidates:
                    print(f"[!] d2c: libagpjnix.so not found after build for {abi} ({suffix})")
                    if use_ollvm and not ollvm_required:
                        print(f"[!] d2c Hikari output missing for {abi}; retrying normal NDK compile")
                        continue
                    continue

                # Copy to output_dir/lib/<abi>/.
                dest_dir = output_dir / "lib" / abi
                dest_dir.mkdir(parents=True, exist_ok=True)
                dest = dest_dir / "libagpjnix.so"
                shutil.copy2(str(so_candidates[0]), str(dest))
                results[abi] = dest
                print(f"[d2c] built {abi} ({suffix}): {dest}")
                if build_report is not None:
                    item = {
                        "abi": abi,
                        "library": "libagpjnix.so",
                        "path": str(dest),
                        "mode": "ollvm" if use_ollvm else "ndk",
                        "ollvm_protected": bool(use_ollvm),
                    }
                    cast_per_abi = build_report.setdefault("per_abi", [])
                    if isinstance(cast_per_abi, list):
                        cast_per_abi.append(item)
                    lib_path = f"lib/{abi}/libagpjnix.so"
                    key = "ollvm_protected_libraries" if use_ollvm else "fallback_libraries"
                    abi_key = "ollvm_protected_abis" if use_ollvm else "fallback_abis"
                    libs = build_report.setdefault(key, [])
                    abi_list = build_report.setdefault(abi_key, [])
                    if isinstance(libs, list) and lib_path not in libs:
                        libs.append(lib_path)
                    if isinstance(abi_list, list) and abi not in abi_list:
                        abi_list.append(abi)
                    if not use_ollvm and ollvm_enabled:
                        build_report["fallback_used"] = True
                abi_built = True
                break

            if not abi_built:
                print(f"[!] d2c: no libagpjnix.so produced for {abi}")

    return results


