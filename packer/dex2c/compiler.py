def run_dex2c_compilation(
    dex_files: list[Path],
    targets: list[tuple[str, str, str | None]],
    ndk_path: str,
    decoded_apk_dir: Path,
    fail_open: bool = False,
    wipe_insns: bool = True,
    target_abis: list[str] | None = None,
    ollvm_enabled: bool = True,
    ollvm_clang: str | None = None,
    ollvm_required: bool = False,
    build_report: dict[str, object] | None = None,
) -> tuple[int, int]:
    """Translate target methods to C, compile to .so, patch DEX.

    Returns ``(compiled_count, so_count)``.
    """
    if not targets:
        return 0, 0

    ndk_path = (ndk_path or "").strip()
    if not ndk_path:
        msg = "--ndk-path is required for DEX2C compilation"
        if fail_open:
            print(f"[!] {msg}")
            return 0, 0
        raise RuntimeError(msg)

    if not Path(ndk_path).is_dir():
        msg = f"NDK path does not exist: {ndk_path}"
        if fail_open:
            print(f"[!] {msg}")
            return 0, 0
        raise RuntimeError(msg)

    # ── Parse DEX files ──
    parsed: list[DexFile] = []
    for dex_path in dex_files:
        parsed.append(parse_dex(dex_path.read_bytes()))

    # ── Translate target methods ──
    translated: list[D2CMethod] = []
    compiled_indices: dict[int, list[int]] = {}  # dex_idx → [method_idx, ...]
    compiled_set: set[tuple[int, int]] = set()  # (dex_idx, method_idx)
    d2c_skipped_by_reason: dict[str, int] = {}

    for class_desc, method_name, sig in targets:
        matched = False

        for dex_idx, dex in enumerate(parsed):
            candidates: list[EncodedMethod] = []

            # Wildcard: translate all methods in the class.
            if method_name == "*":
                candidates = _iter_class_methods(dex, class_desc)
                if not candidates:
                    continue
                matched = True

            # No signature provided: translate all overloads of method_name in the class.
            elif sig is None:
                ems = _iter_class_methods(dex, class_desc)
                if not ems:
                    continue
                candidates = [
                    em for em in ems
                    if dex.method_name(em.method_idx) == method_name
                ]
                if not candidates:
                    continue
                matched = True

            # Exact match.
            else:
                midx = dex.find_method(class_desc, method_name, sig)
                if midx is None:
                    continue
                matched = True

                em = _find_encoded_method(dex, midx)
                if em is None or em.code is None:
                    msg = f"d2c: no code for {class_desc}->{method_name}{sig}"
                    if fail_open:
                        print(f"[!] {msg}")
                        break
                    raise RuntimeError(msg)

                candidates = [em]

            for em in sorted(candidates, key=lambda x: x.method_idx):
                midx = em.method_idx
                if (dex_idx, midx) in compiled_set:
                    continue

                if em.code is None:
                    msg = f"d2c: no code for {class_desc}->{dex.method_name(midx)}"
                    if method_name == "*" or sig is None:
                        print(f"[!] {msg}; skip")
                        continue
                    if fail_open:
                        print(f"[!] {msg}")
                        continue
                    raise RuntimeError(msg)

                if em.code.insns_size == 0:
                    continue

                eligible, reason = method_d2c_eligibility(em.code)
                if not eligible:
                    # Method uses opcodes DEX2C cannot translate (typically
                    # Kotlin lambda dispatch / invoke-custom). Tally the
                    # reason and skip; the harden orchestrator will see this
                    # method missing from compiled_indices and route it to
                    # VMP or extract instead. Without this pre-check the
                    # translator would raise NotImplementedError partway
                    # through and the method would silently end up
                    # unprotected.
                    d2c_skipped_by_reason[reason] = (
                        d2c_skipped_by_reason.get(reason, 0) + 1
                    )
                    continue

                try:
                    m = translate_method(dex, midx, em)
                    translated.append(m)
                    compiled_indices.setdefault(dex_idx, []).append(midx)
                    compiled_set.add((dex_idx, midx))
                except Exception as exc:
                    msg = (
                        f"d2c: translate failed {class_desc}->{dex.method_name(midx)} "
                        f"(idx={midx}): {exc}"
                    )
                    if fail_open:
                        print(f"[!] {msg}")
                        continue
                    raise RuntimeError(msg) from exc

            break  # first DEX that has it

        if not matched:
            msg = f"d2c: method not found: {class_desc}->{method_name}" + (sig or "")
            if fail_open:
                print(f"[!] {msg}")
            else:
                raise RuntimeError(msg)

    if d2c_skipped_by_reason:
        total_skipped = sum(d2c_skipped_by_reason.values())
        detail = ", ".join(
            f"{reason}={count}" for reason, count in
            sorted(d2c_skipped_by_reason.items(), key=lambda kv: -kv[1])
        )
        print(
            f"[d2c] {total_skipped} method(s) routed away from DEX2C "
            f"due to unsupported Dalvik opcodes ({detail}); "
            f"these should fall back to VMP or method extract."
        )

    if not translated:
        return 0, 0

    print(f"[d2c] translated {len(translated)} method(s)")

    # ── Generate C source ──
    c_source = generate_c_source(translated)

    # ── Detect which ABIs the decoded APK has ──
    compile_abis: list[str] | None = target_abis
    if compile_abis is None:
        lib_root = decoded_apk_dir / "lib"
        if lib_root.is_dir():
            existing = sorted(p.name for p in lib_root.iterdir() if p.is_dir())
            if existing:
                compile_abis = existing

    # ── Compile with NDK ──
    try:
        so_map = compile_with_ndk(
            c_source,
            ndk_path,
            decoded_apk_dir,
            abis=compile_abis,
            ollvm_enabled=ollvm_enabled,
            ollvm_clang=ollvm_clang,
            ollvm_required=ollvm_required,
            build_report=build_report,
        )
    except Exception as exc:
        msg = f"d2c: NDK compilation failed: {exc}"
        if fail_open:
            print(f"[!] {msg}")
            return len(translated), 0
        raise RuntimeError(msg) from exc

    if not so_map:
        msg = "d2c: NDK compilation produced no .so files"
        if fail_open:
            print(f"[!] {msg}")
            return len(translated), 0
        raise RuntimeError(msg)

    # ── Patch DEX: mark compiled methods as ACC_NATIVE ──
    for dex_idx, method_indices in compiled_indices.items():
        dex = parsed[dex_idx]
        patched = patch_methods_to_native(dex, method_indices, wipe_insns=wipe_insns)
        dex_files[dex_idx].write_bytes(patched)
        print(
            f"[d2c] patched {dex_files[dex_idx].name}: "
            f"{len(method_indices)} method(s) → native"
        )

    return len(translated), len(so_map)
