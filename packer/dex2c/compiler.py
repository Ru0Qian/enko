def _find_encoded_method(dex: DexFile, method_idx: int) -> EncodedMethod | None:
    for cd in dex.class_defs:
        if cd.class_data is None:
            continue
        for em in cd.class_data.direct_methods + cd.class_data.virtual_methods:
            if em.method_idx == method_idx:
                return em
    return None


def _iter_class_methods(dex: DexFile, class_desc: str) -> list[EncodedMethod]:
    """Return all methods defined on a class (direct + virtual)."""
    out: list[EncodedMethod] = []
    for cd in dex.class_defs:
        if dex.type_name(cd.class_idx) != class_desc:
            continue
        if cd.class_data is None:
            continue
        out.extend(cd.class_data.direct_methods)
        out.extend(cd.class_data.virtual_methods)
    return out


def _desc_to_jni_slash(desc: str) -> str:
    """``Lcom/example/Foo;`` → ``com/example/Foo``."""
    if desc.startswith("L") and desc.endswith(";"):
        return desc[1:-1]
    return desc


def _c_escape(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n")


