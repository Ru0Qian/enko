_C_HEADER = """\
/* AUTO-GENERATED by Enko DEX2C compiler — do not edit. */
#include <jni.h>
#include <string.h>
#include <stdlib.h>
#include <stdint.h>
#include <math.h>

typedef uint32_t juint;
typedef uint64_t julong;

/* ---- Cached payload ClassLoader for class resolution fallback ---- */
static jobject     g_d2c_loader      = NULL;
static jmethodID   g_d2c_loadClassM  = NULL;

/* Try FindClass first; fall back to ClassLoader.loadClass(). */
static jclass d2c_find_class(JNIEnv *env, const char *slash_name) {
    jclass clz = (*env)->FindClass(env, slash_name);
    if (clz) return clz;
    /* FindClass failed — clear pending exception. */
    if ((*env)->ExceptionCheck(env)) (*env)->ExceptionClear(env);
    if (!g_d2c_loader || !g_d2c_loadClassM) return NULL;
    /* Convert slash form to dot form for loadClass. */
    size_t len = strlen(slash_name);
    char *dot = (char *)malloc(len + 1);
    if (!dot) return NULL;
    for (size_t i = 0; i < len; i++)
        dot[i] = (slash_name[i] == '/') ? '.' : slash_name[i];
    dot[len] = '\\0';
    jstring jn = (*env)->NewStringUTF(env, dot);
    free(dot);
    if (!jn) return NULL;
    jclass r = (jclass)(*env)->CallObjectMethod(env, g_d2c_loader, g_d2c_loadClassM, jn);
    (*env)->DeleteLocalRef(env, jn);
    if ((*env)->ExceptionCheck(env)) {
        (*env)->ExceptionClear(env);
        return NULL;
    }
    return r;
}

/* ---- Translated method bodies ---- */

"""

_C_REG_STRUCT = """\

/* ---- Registration table ---- */

typedef struct {
    const char *class_desc;   /* e.g. "com/example/Foo" (slash form) */
    const char *method_name;
    const char *signature;    /* JNI signature e.g. "(II)V" */
    int         is_static;
    void       *func_ptr;
} d2c_entry_t;

"""


def generate_c_source(methods: list[D2CMethod]) -> str:
    """Generate a complete .c file from translated methods."""
    parts: list[str] = [_C_HEADER]

    # Emit each translated function body.
    for m in methods:
        parts.append(m.c_body)
        parts.append("\n\n")

    # Emit registration struct and table.
    parts.append(_C_REG_STRUCT)

    parts.append(f"static const d2c_entry_t g_d2c_table[] = {{\n")
    for m in methods:
        cls = _c_escape(_desc_to_jni_slash(m.class_desc))
        name = _c_escape(m.method_name)
        sig = _c_escape(m.jni_sig)
        static_flag = 1 if m.is_static else 0
        parts.append(
            f'    {{"{cls}", "{name}", "{sig}", {static_flag}, (void *){m.func_name}}},\n'
        )
    parts.append("};\n\n")

    parts.append(f"static const int g_d2c_count = {len(methods)};\n\n")

    # Emit the registration entry-point function.
    parts.append(_REGISTRATION_FUNC)

    return "".join(parts)


_REGISTRATION_FUNC = """\
/* ---- Entry point called from libagpcore.so via dlsym ---- */

__attribute__((visibility("default")))
int enko_d2c_init(JNIEnv *env, jobject classLoader) {
    if (!env || !classLoader) return -1;

    /* Get ClassLoader.loadClass(String) method. */
    jclass loaderClz = (*env)->GetObjectClass(env, classLoader);
    if (!loaderClz) return -1;
    jmethodID loadClassM = (*env)->GetMethodID(
        env, loaderClz, "loadClass",
        "(Ljava/lang/String;)Ljava/lang/Class;");
    (*env)->DeleteLocalRef(env, loaderClz);
    if (!loadClassM) return -1;

    /* Cache for d2c_find_class() fallback. */
    g_d2c_loader     = (*env)->NewGlobalRef(env, classLoader);
    g_d2c_loadClassM = loadClassM;

    int registered = 0;

    for (int i = 0; i < g_d2c_count; i++) {
        const d2c_entry_t *e = &g_d2c_table[i];

        /* Convert slash class name to dot form for ClassLoader.loadClass. */
        size_t len = strlen(e->class_desc);
        char *dotName = (char *)malloc(len + 1);
        if (!dotName) continue;
        for (size_t j = 0; j < len; j++) {
            dotName[j] = (e->class_desc[j] == '/') ? '.' : e->class_desc[j];
        }
        dotName[len] = '\\0';

        jstring jName = (*env)->NewStringUTF(env, dotName);
        free(dotName);
        if (!jName) continue;

        jclass targetClz = (jclass)(*env)->CallObjectMethod(
            env, classLoader, loadClassM, jName);
        (*env)->DeleteLocalRef(env, jName);

        if ((*env)->ExceptionCheck(env)) {
            (*env)->ExceptionClear(env);
            continue;
        }
        if (!targetClz) continue;

        JNINativeMethod nm;
        nm.name = (char *)e->method_name;
        nm.signature = (char *)e->signature;
        nm.fnPtr = e->func_ptr;

        jint rc = (*env)->RegisterNatives(env, targetClz, &nm, 1);
        (*env)->DeleteLocalRef(env, targetClz);

        if (rc == 0) {
            registered++;
        } else {
            if ((*env)->ExceptionCheck(env)) {
                (*env)->ExceptionClear(env);
            }
        }
    }

    return registered;
}
"""


